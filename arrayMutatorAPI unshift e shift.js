//funcao unshift e shift

const languages = ['Python', 'C', 'Java'];
console.log(languages);
console.log(languages.unshift('Ruby'));//ira colocar o ruby no inicio do vetor
console.log(languages.unshift('Go'));
console.log(languages);
console.log(languages.shift()); //remove o primeiro item do vetor
console.log(languages.shift());
